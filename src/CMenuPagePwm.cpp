#include "Arduino.h"

#include "CMenu_Classes.h"

/***************************************************************************/
/*                                                                         */
/*                                                                         */
/* CMenuPagePwm::CMenuPagePwm()                                            */
/*                                                                         */
/*                                                                         */
/***************************************************************************/
CMenuPagePwm::CMenuPagePwm()
{
    m_PwmValue = 0;
}

/***************************************************************************/
/*                                                                         */
/*                                                                         */
/* CMenuPagePwm::IncrementValue()                                          */
/*                                                                         */
/*                                                                         */
/***************************************************************************/
void CMenuPagePwm::IncrementValue()
{
    if( m_PwmValue < MAX_PWM ) m_PwmValue += 5;
}

/***************************************************************************/
/*                                                                         */
/*                                                                         */
/* CMenuPagePwm::DecrementValue()                                          */
/*                                                                         */
/*                                                                         */
/***************************************************************************/
void CMenuPagePwm::DecrementValue()
{
    if( m_PwmValue > 0) m_PwmValue -= 5;
}
